public class ComparisonOperator {
    public static void main(String[] args) {
        //비교연산자
        int age = 20;
        System.out.println(age > 19);
        System.out.println(age < 19);
        System.out.println(age >= 20);
        System.out.println(age <= 20);
        System.out.println(age == 19);
        System.out.println(age != 19);
    }
}
